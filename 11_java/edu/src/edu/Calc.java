package edu;

public class Calc {

	public static void main(String[] args) {
		int A1 = 10, A2 = 20;
		int A3 = A1 + A2;
		
		System.out.println(A3);
		
		int a = 7, b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
	}

}
