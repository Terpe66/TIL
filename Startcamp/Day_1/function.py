first = [11.25, 18.0, 20.0]
second = [10.75, 9.50]

# full 에 first 와 second 를 합쳐서 저장
full = first + second
print(full)

# full_sorted 에 full 을 정렬해서 저장
full_sorted = sorted(full)
print(full_sorted)

# *reverse_sorted 에 full 을 내림차순으로 정렬해서 저장
reverse_sorted = sorted(full, reverse=True)
print(reverse_sorted)

